interface Seed {
  id: string
  name: string
  categoryId: string
  sellerProfileId: string
  brand: string
  unit: MaterialUnit
  price: number
  mrp: number
  stock: number
  rating: number
  ratingCount: number
  shortDescription: string
  specs: Record<string, string>
  tags: string[]
  featured?: boolean
}

function slugify(s: string): string {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')
}

function build(seed: Seed, i: number): Product {
  const slug = slugify(seed.name)
  return {
    ...seed,
    slug,
    images: productImages(seed.id, seed.categoryId, i),
    description: `${seed.shortDescription} Sourced from verified suppliers and quality-checked before dispatch. Ideal for residential and commercial construction projects. Bulk pricing available on request.`,
    featured: seed.featured ?? false,
    createdAt: new Date(2024, 5, (i % 27) + 1).toISOString(),
  }
}

